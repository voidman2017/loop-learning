def parse_output(output: str) -> dict:
    """
    解析 LLM 输出，提取变更摘要 JSON。
    如果输出末尾包含 JSON 摘要，则解析并返回；否则返回空摘要。
    """
    # 尝试从输出末尾提取 JSON
    json_match = re.search(r'```json\n(.+?)\n```', output, re.DOTALL)
    if json_match:
        try:
            return json.loads(json_match.group(1))
        except json.JSONDecodeError:
            pass
    return {
        "change_summary": "No structured summary available",
        "files_changed": [],
        "risk_level": "low",
        "new_review_items": 0,
        "archived_items": 0,
        "uncertain_claims": [],
        "sources_used": []
    }


def write_outputs(output: str, context: dict) -> dict:
    """将 LLM 输出写入对应的目标文件。"""
    date_str = context["current_date"]

    # 1. 写入今日日报
    daily_file = DAILY_DIR / f"{date_str}.md"
    write_file(daily_file, output)

    # 2. 写入今日仪表盘（简单提取第一段作为摘要）
    first_line = output.strip().split("\n")[0] if output.strip() else "No content"
    dashboard = f"""# Today's Dashboard

{first_line}

## Quick Stats

- Date: {date_str}
- Status: Report generated

## Recent Activity

{output}
"""
    dashboard_file = DASHBOARDS_DIR / "today.md"
    write_file(dashboard_file, dashboard)

    # 3. 解析变更摘要
    summary = parse_output(output)

    return summary


def validate_outputs() -> bool:
    """调用 validate_outputs.py 验证输出。"""
    import subprocess
    result = subprocess.run(
        [sys.executable, str(REPO_ROOT / "scripts" / "validate_outputs.py")],
        capture_output=True,
        text=True,
    )
    if result.returncode != 0:
        print(f"Validation failed:\\n{result.stderr}")
        return False
    print("Validation passed.")
    return True


def main():
    """Daily Learning Loop 主流程。"""
    print("=== Daily Learning Loop ===")
    print(f"Time: {datetime.now(timezone.utc).isoformat()}")

    # Step 1: 构建上下文
    print("\\n[1/5] Building context...")
    context = build_context()
    print(f"  Date: {context['current_date']}")
    print(f"  Notes dir empty: {context['notes_dir_empty']}")
    print(f"  Research dir empty: {context['research_dir_empty']}")

    # Step 2: 构建 prompt
    print("\\n[2/5] Building system prompt...")
    system_prompt = build_system_prompt(context)

    # Step 3: 调用 LLM
    print("\\n[3/5] Calling LLM...")
    output = call_llm(system_prompt, context)
    print(f"  Output length: {len(output)} chars")

    # Step 4: 写入输出
    print("\\n[4/5] Writing outputs...")
    summary = write_outputs(output, context)
    print(f"  Files changed: {summary.get('files_changed', [])}")
    print(f"  Risk level: {summary.get('risk_level', 'unknown')}")

    # Step 5: 验证输出
    print("\\n[5/5] Validating outputs...")
    valid = validate_outputs()
    if not valid:
        print("ERROR: Output validation failed!")
        sys.exit(1)

    print("\\n=== Daily Learning Loop Complete ===")
    return summary


if __name__ == "__main__":
    summary = main()
    print(f"\\nChange summary: {json.dumps(summary, ensure_ascii=False, indent=2)}")