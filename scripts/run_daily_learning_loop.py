"""
Daily Learning Loop — 核心运行脚本

职责：
1. 读取 STATE.md、learning-state.json、最近日报、笔记、研究草稿、复习队列
2. 调用 LLM 生成今日日报、复习题与状态更新
3. 输出到 content/reports/daily/、content/dashboards/、state/、REVIEW-QUEUE.md、STATE.md

注意：本脚本不直接修改 .github/、scripts/、site/、skills/、prompts/、loop-* 文件。
"""

import json
import os
import re
import sys
from datetime import datetime, timezone
from pathlib import Path

# 项目根目录
REPO_ROOT = Path(__file__).resolve().parent.parent

# 路径常量
STATE_MD = REPO_ROOT / "STATE.md"
STATE_JSON = REPO_ROOT / "state" / "learning-state.json"
REVIEW_QUEUE_MD = REPO_ROOT / "REVIEW-QUEUE.md"
REVIEW_QUEUE_JSON = REPO_ROOT / "state" / "review-queue.json"
NOTES_DIR = REPO_ROOT / "content" / "notes"
RESEARCH_DIR = REPO_ROOT / "content" / "research"
DAILY_DIR = REPO_ROOT / "content" / "reports" / "daily"
DASHBOARDS_DIR = REPO_ROOT / "content" / "dashboards"
PROMPT_FILE = REPO_ROOT / "prompts" / "daily-learning-loop.prompt.md"
SKILL_FILE = REPO_ROOT / "skills" / "daily-learning-triage.md"


def read_file(path: Path) -> str:
    """安全读取文件内容，不存在时返回空字符串。"""
    if path.exists():
        return path.read_text(encoding="utf-8")
    return ""


def write_file(path: Path, content: str) -> None:
    """写入文件，自动创建父目录。"""
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def get_recent_daily_reports(days: int = 3) -> list[dict]:
    """获取最近 N 天的日报文件列表。"""
    if not DAILY_DIR.exists():
        return []
    files = sorted(DAILY_DIR.glob("*.md"), reverse=True)
    reports = []
    for f in files[:days]:
        reports.append({
            "path": str(f.relative_to(REPO_ROOT)),
            "content": f.read_text(encoding="utf-8") if f.exists() else ""
        })
    return reports


def build_context() -> dict:
    """构建 LLM 调用的完整上下文。"""
    return {
        "state_md": read_file(STATE_MD),
        "state_json": read_file(STATE_JSON),
        "review_queue_md": read_file(REVIEW_QUEUE_MD),
        "review_queue_json": read_file(REVIEW_QUEUE_JSON),
        "recent_reports": get_recent_daily_reports(3),
        "notes_dir_empty": not any(NOTES_DIR.glob("*.md")) if NOTES_DIR.exists() else True,
        "research_dir_empty": not any(RESEARCH_DIR.glob("*.md")) if RESEARCH_DIR.exists() else True,
        "skill": read_file(SKILL_FILE),
        "prompt_template": read_file(PROMPT_FILE),
        "current_date": datetime.now(timezone.utc).strftime("%Y-%m-%d"),
    }


def build_system_prompt(context: dict) -> str:
    """
    组装 system prompt。
    将 prompt 模板中的 {{date}} 替换为当前日期。
    """
    date_str = context["current_date"]
    prompt = context["prompt_template"]
    prompt = prompt.replace("{{date}}", date_str)

    return prompt


